def write_document(path: Path, title: str, body: str) -> None:
    document = Document()
    document.styles["Normal"].font.name = "Microsoft YaHei"
    document.styles["Normal"].font.size = Pt(10.5)
    document.add_heading(title, level=0)
    document.add_paragraph(body)
    document.add_heading("验证要点", level=1)
    document.add_paragraph("内容应被纳入综合报告，并与其他资料进行交叉归纳。", style="List Bullet")
    document.save(path)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("output", type=Path)
    args = parser.parse_args()
    args.output.mkdir(parents=True, exist_ok=True)
    for filename, (title, body) in DOCUMENTS.items():
        write_document(args.output / filename, title, body)
    intermediate = args.output / "中间步骤"
    intermediate.mkdir(exist_ok=True)
    write_document(
        intermediate / "不应纳入汇总.docx",
        "中间步骤（应排除）",
        "EXCLUDED_INTERMEDIATE_MARKER_7F3A。本文件只用于验证目录排除语义，不得出现在最终汇总中。",
    )
    long_text = "\n\n".join(section * 3 for section in LONG_TEXT_SECTIONS) + "\n"
    (args.output / "阅读材料.txt").write_text(long_text, encoding="utf-8")
    (args.output / "约2000字测试文本.txt").write_text(long_text, encoding="utf-8")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
